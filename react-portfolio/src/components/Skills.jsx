// ── Marquee Row ───────────────────────────────────────────────
function MarqueeRow({ skills, direction = 'left', label, speed = 35 }) {
  const trackRef = useRef(null);
  const animRef   = useRef(null);

  useEffect(() => {
    const track = trackRef.current;
    if (!track) return;

    const totalW = track.scrollWidth / 3; // one copy width (3 copies total)
    const xStart = direction === 'left' ? 0     : -totalW;
    const xEnd   = direction === 'left' ? -totalW : 0;

    // Set initial position
    gsap.set(track, { x: xStart });

    // Infinite loop tween
    animRef.current = gsap.to(track, {
      x: xEnd,
      duration: speed,
      ease: 'none',
      repeat: -1,
      modifiers: {
        x: gsap.utils.unitize((x) => {
          const v = parseFloat(x);
          if (direction === 'left') {
            return v < -totalW ? v + totalW : v;
          } else {
            return v > 0 ? v - totalW : v;
          }
        }),
      },
    });

    // Pause on hover
    track.closest('.marquee-wrapper').addEventListener('mouseenter', () => animRef.current?.pause());
    track.closest('.marquee-wrapper').addEventListener('mouseleave', () => animRef.current?.resume());

    return () => animRef.current?.kill();
  }, [direction, speed]);

  return (
    <div className="marquee-row-container">
      {/* Row label */}
      <div className="marquee-label">
        <span>{label}</span>
      </div>

      <div className="marquee-wrapper overflow-hidden relative">
        {/* Fade edges */}
        <div className="marquee-fade-left" />
        <div className="marquee-fade-right" />

        {/* Moving track */}
        <div ref={trackRef} className="marquee-track flex items-stretch gap-4 py-2">
          {DUP(skills).map((skill, i) => (
            <SkillChip key={`${skill.name}-${i}`} skill={skill} />
          ))}
        </div>
      </div>
    </div>
  );
}

